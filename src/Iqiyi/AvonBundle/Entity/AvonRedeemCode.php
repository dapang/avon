<?php

namespace Iqiyi\AvonBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * AvonRedeemCode
 */
class AvonRedeemCode
{
    /**
     * @var integer
     */
    private $redeemCodeId;

    /**
     * @var string
     */
    private $code;

    /**
     * @var boolean
     */
    private $status;


    /**
     * Get redeemCodeId
     *
     * @return integer 
     */
    public function getRedeemCodeId()
    {
        return $this->redeemCodeId;
    }

    /**
     * Set code
     *
     * @param string $code
     * @return AvonRedeemCode
     */
    public function setCode($code)
    {
        $this->code = $code;

        return $this;
    }

    /**
     * Get code
     *
     * @return string 
     */
    public function getCode()
    {
        return $this->code;
    }

    /**
     * Set status
     *
     * @param boolean $status
     * @return AvonRedeemCode
     */
    public function setStatus($status)
    {
        $this->status = $status;

        return $this;
    }

    /**
     * Get status
     *
     * @return boolean 
     */
    public function getStatus()
    {
        return $this->status;
    }
}
